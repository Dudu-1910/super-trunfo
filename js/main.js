let cartaMaquina;
let cartaJogador;


function sortearCarta() {
  let numeroCartaMaquina = parseInt(Math.random() * 31);
  cartaMaquina = cartas[numeroCartaMaquina];

  let numeroCartaJogador = parseInt(Math.random() * 31);
  while (numeroCartaMaquina === numeroCartaJogador) {
    numeroCartaJogador = parseInt(Math.random() * 31);
  }

  cartaJogador = cartas[numeroCartaJogador];
  console.log(cartaJogador);

  document.querySelector("#btn-sortear").disabled = true;
  document.querySelector("#btn-jogar").disabled = false;
  exibirCartaJogador();
}

function obtemAtributoSelecionado() {
  let radioAtributos = document.getElementsByName("atributo");

  for (let i = 0; i < radioAtributos.length; i++) {
    if (radioAtributos[i].checked === true) {
      return radioAtributos[i].value;
    }
  }
}

function jogar() {
  let atributoSelecionado = obtemAtributoSelecionado();
  let divResultado = document.querySelector("#resultado");

  if (
    cartaJogador.atributos[atributoSelecionado] >
    cartaMaquina.atributos[atributoSelecionado]
  ) {
    htmlResultado = "<p class='resultado-final'> Venceu </p>";
  } else if (
    cartaJogador.atributos[atributoSelecionado] <
    cartaMaquina.atributos[atributoSelecionado]
  ) {
    htmlResultado = "<p class='resultado-final'> Perdeu </p>";
  } else {
    htmlResultado = "<p class='resultado-final'> Empatou </p>";
  }
  divResultado.innerHTML = htmlResultado;

  document.querySelector("#btn-jogar").disabled = true;
  exibirCartaMaquina();
}

function exibirCartaJogador() {
  let divCartaJogador = document.querySelector("#carta-jogador");
  divCartaJogador.style.backgroundImage = `url(${cartaJogador.imagem})`;
  let moldura ='<img src="./img/card-super-trunfo-transparent-ajustado.png" style="width: inherit; height: inherit; position: absolute;">';
  let tagHTML = '<div id="opcoes" class="carta-status">';

  let opcoesTexto = "";
  for (let atributo in cartaJogador.atributos) {
    opcoesTexto += "<input type='radio' name='atributo' value='" + atributo + "'>" + atributo + " " + cartaJogador.atributos[atributo] + "<br>";
  }
  let nome = `<p class="carta-subtitle">${cartaJogador.nome}</p>`;
  divCartaJogador.innerHTML = moldura + nome + tagHTML + opcoesTexto + "</div>";
}

